async def main():
    parser = argparse.ArgumentParser(
        description='ms-agent x OpenClaw MCP integration test',
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog='Examples:\n'
               '  python test_openclaw_mcp.py --list        # list tools\n'
               '  python test_openclaw_mcp.py --test ws     # web search\n'
               '  python test_openclaw_mcp.py --test fs,ws  # combined\n'
               '  python test_openclaw_mcp.py               # all tests\n',
    )
    parser.add_argument('--list', action='store_true',
                        help='list available MCP tools only')
    parser.add_argument(
        '--test', default='all',
        help=f'tests to run (comma-separated): '
             f'{", ".join(TESTS)} or all (default: all)',
    )
    args = parser.parse_args()

    try:
        from mcp import ClientSession  # noqa: F401
    except ImportError:
        print('ERROR: mcp package not installed.  pip install mcp')
        sys.exit(1)

    from contextlib import AsyncExitStack

    async with AsyncExitStack() as stack:
        session = await connect(stack)
        tools = await list_tools(session)

        if args.list:
            return

        if args.test == 'all':
            selected = list(TESTS.keys())
        else:
            selected = [t.strip() for t in args.test.split(',')]
            unknown = [t for t in selected if t not in TESTS]
            if unknown:
                print(f'ERROR: unknown test(s): {unknown}')
                print(f'Available: {", ".join(TESTS)}')
                sys.exit(1)

        passed, failed_list = 0, []
        for key in selected:
            test_fn, name = TESTS[key]
            try:
                await test_fn(session)
                passed += 1
            except Exception as e:
                import traceback
                fail(f'{name} crashed: {e}')
                traceback.print_exc()
                failed_list.append(name)

        total = len(selected)
        print(f'\n{"=" * 64}')
        print(f'  Result: {passed}/{total} passed', end='')
        if failed_list:
            print(f', failed: {", ".join(failed_list)}')
        else:
            print(' -- ALL PASSED')
        print(f'{"=" * 64}\n')


if __name__ == '__main__':
    asyncio.run(main())
