"""
ms-agent × OpenClaw  —  MCP integration test

Validates that the ms-agent MCP server works correctly when spawned
the same way OpenClaw would launch it (stdio subprocess with PYTHONPATH).

The test connects as a generic MCP client — identical JSON-RPC calls
regardless of whether OpenClaw, Hermes, nanobot, or Cursor is the host.

Usage:
  python test_openclaw_mcp.py               # run all tests
  python test_openclaw_mcp.py --list        # list tools only
  python test_openclaw_mcp.py --test ws     # test web_search only
  python test_openclaw_mcp.py --test fs,ws  # combined tests
"""
import argparse
